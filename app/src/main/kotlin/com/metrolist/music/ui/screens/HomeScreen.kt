@OptIn(ExperimentalFoundationApi::class, ExperimentalMaterial3Api::class, ExperimentalMaterial3ExpressiveApi::class)
@Composable
fun HomeScreen(
    navController: NavController,
    snackbarHostState: SnackbarHostState,
    viewModel: HomeViewModel = hiltViewModel(),
) {
    // Preferences for Dynamic Gradient
    val (gradientBackgroundEnabled, _) = rememberPreference(
        GradientBackgroundEnabledKey, defaultValue = false)
    val (gradientBackgroundOpacity, _) = rememberPreference(
        GradientBackgroundOpacityKey, defaultValue = 0.15f)

    val menuState = LocalMenuState.current
    val bottomSheetPageState = LocalBottomSheetPageState.current
    val database = LocalDatabase.current
    val playerConnection = LocalPlayerConnection.current ?: return
    val haptic = LocalHapticFeedback.current
    val listenTogetherManager = LocalListenTogetherManager.current
    val isListenTogetherGuest = listenTogetherManager?.let { it.isInRoom && !it.isHost } ?: false

    val isPlaying by playerConnection.isEffectivelyPlaying.collectAsStateWithLifecycle()
    val mediaMetadata by playerConnection.mediaMetadata.collectAsStateWithLifecycle()
    
    // Dynamic color extraction logic
    val currentSongAlbumArt = mediaMetadata?.let { metadata ->
        remember(metadata) { mutableStateOf(metadata.albumArt) }
    }

    val accentColor by produceState(
        initialValue = Color.Transparent,
        key1 = currentSongAlbumArt?.value,
        key2 = gradientBackgroundEnabled
    ) {
        if (gradientBackgroundEnabled && currentSongAlbumArt?.value != null) {
            val extracted = ColorExtractor.extractAccentColor(currentSongAlbumArt.value)
            value = extracted.copy(alpha = gradientBackgroundOpacity)
        } else {
            value = Color.Transparent
        }
    }

    // ... (rest of the state variables and side effects remain unchanged)

    val scope = rememberCoroutineScope()
    // ... (logic for localGridItem and ytGridItem remains unchanged)

    DynamicGradientBackground(
        isEnabled = gradientBackgroundEnabled,
        accentColor = accentColor,
        modifier = Modifier.fillMaxSize(),
    ) {
        PullToRefreshBox(
            state = pullRefreshState,
            isRefreshing = isRefreshing,
            onRefresh = viewModel::refresh,
            indicator = {
                Indicator(
                    isRefreshing = isRefreshing,
                    state = pullRefreshState,
                    modifier =
                        Modifier
                            .align(Alignment.TopCenter)
                            .padding(LocalPlayerAwareWindowInsets.current.asPaddingValues()),
                )
            },
        ) {
            BoxWithConstraints(
                modifier = Modifier.fillMaxSize(),
                contentAlignment = Alignment.TopStart,
            ) {
                // ... (LazyColumn and the rest of the UI code remains unchanged)
            }
        }
    }
}
